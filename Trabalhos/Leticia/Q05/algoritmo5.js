function adiciona_hifen(entrada){

  let resultado = '';

  for (let i = 0; i < entrada.length; i++){
    //concatena o número antes do hífen.
    // console.log('num',entrada[i] )
    
    if (i != 0){
      if (entrada[i] % 2 == 0 && entrada[i-1] % 2 == 0)
        resultado += '-';
    }
    resultado += entrada[i];
  }
  console.log(`"${resultado}",`);
  return resultado;
}

